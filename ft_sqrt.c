#include "libft.h"

size_t	ft_sqrt(size_t nb)
{
	size_t	i;
	size_t	s;

	i = 0;
	s = 0;
	while (s != nb)
	{
		i++;
		s = i * i;
		if (s > nb)
			return (0);
	}
	return (i);
}
