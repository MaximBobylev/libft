#include "libft.h"

void	ft_bzero(void *mem, size_t n)
{
	if (n)
		n--;
	else
		return ;
	while (n > 0)
	{
		*(unsigned char *)(mem + n) = 0;
		n--;
	}
	*(unsigned char *)(mem + n) = 0;
}
