#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	if (!alst || !*alst || !del)
		return ;
	(void)del((*alst)->content, (*alst)->content_size);
	free((void *)(*alst));
	*alst = NULL;
}
